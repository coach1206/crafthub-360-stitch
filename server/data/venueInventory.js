/**
 * Returns all items for a venue, or filtered by category.
 * @param {string} venueId
 * @param {string|null} category
 */
export function getSampleInventory(venueId, category = null) {
  let items = VENUE_SAMPLE_INVENTORY.filter(i => i.venue_id === venueId)
  if (category) items = items.filter(i => i.item_category === category)
  return items
}

export function getSampleItem(itemId) {
  return VENUE_SAMPLE_INVENTORY.find(i => i.item_id === itemId) || null
}

